#for é feito para percorrer uma lista o for é um interador
#Range(intervalo)
#range(inicio,fim,passo)
for i in range (1,10):# não conta o 10
    print(i)
print('fim do loop')  
for i in range (1,100,2):# não conta o 100
    print(i)
print('fim do loop')  
for i in range (0,100,2):# não conta o 100
    print(i)
print('fim do loop')  
 