//Crie um programa que determine o mostre os 10 primeiros números pares, considerando números maiores que 0.
package lista_exercicios_02;

public class ex3Jow {
    public static void main(String[] args){
        int i=0;
        int x = 0;
        while(i<10){
            x=2*i+2;
            System.out.println(x);
            i++;
        }
    }
    
}
