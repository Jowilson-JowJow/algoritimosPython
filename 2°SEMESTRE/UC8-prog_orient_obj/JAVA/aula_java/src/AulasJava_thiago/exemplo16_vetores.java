package AulasJava_thiago;

public class exemplo16_vetores {
    public static void main(String[] args) {
        
        int[] vet = new int [10];//cria uma lista(vetor) com 10 possições
        vet[0]=10;//atribui valor 10 a posição 0do vetor
        vet[1]=vet[0]+10;//atribui na posição 1 do vetor o valor da posição 1 +10
        System.out.println(vet[1]);
    }
    
}
